namespace RuStore.BillingClient {

    /// <summary>
    /// Информация о периоде подписки.
    /// </summary>
    public class SubscriptionPeriod {

        /// <summary>
        /// Количество лет.
        /// </summary>
        public int years;

        /// <summary>
        /// Количество месяцев.
        /// </summary>
        public int months;

        /// <summary>
        /// Количество дней.
        /// </summary>
        public int days;
    }
}
